package JavaProgrammerCourse;

public class Module2 {

    /* COMPARISON OPERATORS
    
    <
    >
    <=
    >=
    !=
    ==
    
    */
    public static void main(String[] args) {
        
        int a=500;
        /*--------------------- SINGLE CONDITIONAL -----------------------------
        
        if(a>400){
            System.out.println("THE NUMBER IS GREATER THAN 400.");
        }else{
            System.out.println("THE NUMBER IS NOT GREATER THAN 400.");
        }
        */
        
        // ----------------------- MULTI CONDITIONAL -----------------------------
        if(a>400){
            System.out.println("THE NUMBER IS GREATER THAN 400.");
        }
        
        if(a<700){
            System.out.println("THE NUMBER IS LESSER THAN 700.");
        }
        
        if(a>700){
            System.out.println("THE NUMBER IS LESSER THAN 700.");
        }
    }
    
}
